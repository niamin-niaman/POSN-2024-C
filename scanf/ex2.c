// Exercise 2: ให้เขียนโปรแกรมภาษา C ที่ใช้ scanf เพื่อรับค่าน้ำหนัก (kg) และความสูง (m) จากผู้ใช้ แล้วคำนวณและแสดงผลดัชนีมวลกาย (BMI) โดยใช้สูตร BMI = น้ำหนัก (kg) / (ความสูง (m))^2 

#include <stdio.h>
int main() {
int lon;
  printf("โปรดป้อนความยาวด้านของสี่เหลี่ยมจตุรัส:");
  scanf("%d",&lon);
  
  printf("พื้นที่ของรูปสี่เหลี่ยมจตุรัสคือ: %d ตารางหน่วย", lon*lon);
  return 0;
}